                        <h3>Categorizing the Severity of Your Burn</h3>
                        <p>If it's not clear what level of care is needed, try to judge the extent of tissue damage, based on the following burn categories:
                        </p>
                    </div>


                    <div class="single-post-content">
                        <h3>Part 1: Determine whether you have a first degree burn.</h3>
                        <div class="single-post-image">
                            <img src="img/burn/1.png" style="height:200; width:400px" alt="Post Title">
                        </div>
                        <p>
                            First degree burns are the most common burns. You have a first degree burn if only the outer most layer of skin is affected. These are the least severe type of burn and can usually be treated at home. Symptoms involve:

                        <ul>
                            <li><p>Pain</p></li>
                            <li><p>Swelling</p></li>
                            <li><p>Reddening of the skin</p></li>
                        </ul>
                        </p>

                        <h3>Part 2: Examine whether you have a second degree burn.</h3>
                        <div class="single-post-image">
                            <img src="img/burn/2.png" style="height:200; width:400px" alt="Post Title">
                        </div>
<p>
                        Second degree burns are more serious than first degree burns. The damage goes beneath the outer layer of skin to affect the layer underneath. You may have a scar after it heals. Symptoms of a second degree burn include:

                        <ul>
                        <li><p>Pain</p></li>
                        <li><p>Swelling</p></li>
                        <li><p>Blistering</p></li>
                        <li><p>Red, white, or blotchy skin</p></li>
                        <li><p>The burned area may look wet.</p></li>
                        </ul>
                        </p>
                        <p>
                        <h3>Part 3: Identify a third degree burn.</h3>
                        <div class="single-post-image">
                            <img src="img/burn/3.png" style="height:200; width:400px" alt="Post Title">
                        </div>
                        <p>
                            Third degree burns involve severe damage that includes tissues beneath the skin such as the layer of fat below the skin and possibly even the muscle or bone. Symptoms include:</p>
<ul>
                        <li><p>A waxy or leathery appearance to the skin</p></li>
                        <li><p>Black or white areas on the skin</p></li>
                        <li><p>Numbness where the nerves have been damaged</p></li>
                        <li><p>Breathing problems</p></li>
                        <li><p>Carbon monoxide poisoning</p></li>
                        </ul>


                        <h3>Part 4: Seek medical care if needed.</h3>
                        <div class="single-post-image">
                            <img src="img/burn/4.png" style="height:200; width:400px" alt="Post Title">
                        </div>
                        <p>
                            Someone with a third degree burn is likely to require an ambulance. If you have less serious burns you may still need to go to the emergency room. Seek professional medical care if:
<ul>
                        <li><p>You have a second or third degree burn.</p></li>
                        <li><p>You have a second degree burn that covers more than 3 inches of skin.</p></li>
                        <li><p>You have a first or second degree burn on your hands, feet, face, groin, buttocks, or a joint.</p></li>
                        <li><p>The burn is infected. Infected burns may seep liquid from the wound, and have pain, redness and swelling that gets worse over time.</p></li>
                        <li><p>The burn has extensive blistering.</p></li>
                        <li><p>You have a chemical or electrical burn.</li>
                        <li><p>You have burned your airways or you have problems breathing.</p></li>
                        <li><p>You have severe scarring or a burn that doesn’t heal after a few weeks.</p></li>
                        </ul>
                        </p>
                        <br>

                        <div class="single-post-info">
                            <h3>Treating Minor Burns</h3>
                        </div>
                        <div class="single-post-image">
                            <img src="img/burn/1.png" style="height:200; width:400px" alt="Post Title">
                        </div>
                        <br>
                        <h3>Step 1: Soothe the burn with cold water.</h3>
                        <p>
                            Cool water will lower the temperature of the burned area and stop the damage from occurring. Gently run cool water over the burn for at least 10 minutes.</p>
                        <ul>
                            <li><p>Items that may need to be removed include rings, bracelets, necklaces, anklets, or any other item that could cut off circulation during swelling.</p></li>
                            <li><p>Do not put ice or very cold water on the burn. The extreme temperatures may increase the damage to your tissues.</p></li>
                        </ul>
                        </p>
                        <h3>Step 2: Take off jewelry that is on the affected area.</h3>
                        <p>
                            If you have jewelry or other items that may restrict blood flow if the area swells, remove it immediately.</p>
                        <ul>
                            <li><p>If the movement of water flowing over the burn is too uncomfortable, you can apply a wet towel.</p></li>
                            <li><p>Swelling will start immediately so remove the items as soon as possible, but do so gently to avoid further irritation to the damaged tissues.</p></li>
                        </ul>
                        </p>
                        <h3>Step 3: Smear aloe on the burns that are not open wounds.</h3>
                        <p>
                            The gel from aloe plants reduces pain and inflammation. It also promotes healing and helps your body repair damaged skin. Do not apply it to an open wound.</p>
                        <ul>
                            <li><p>Aloe is found in many gels and moisturizers. If you have a commercially prepared aloe vera gel, apply it according to the manufacturer’s instructions.</p></li>
                            <li><p>If you have an aloe plant in your house, you can obtain the gel directly from the plant. Break off a leaf and split it open lengthwise. You will see a clear, greenish goo inside. Dab it directly onto the burn and let it absorb into the skin.</p></li>
                            <li><p>If you do not have aloe, you can apply another moisturizer to prevent the burn from becoming too dry as it heals.</p></li>
                            <li><p>Don’t put greasy materials like butter on the wound.</p></li>
                        </ul>
                        </p>
                        <h3>Step 4: Don’t pop blisters.</h3>
                        <p>
                            If you pop blisters, this creates an open wound and makes you vulnerable to an infection. If the blisters burst on their own you should:</p>
                        <ul>
                            <li><p>Wash the wound with soap and clean water.</p></li>
                            <li><p>Gently dab an antibiotic cream over the area.</p></li>
                            <li><p>Protect the area with a nonstick bandage.</p></li>
                            <li><p>Go to the doctor if you have blisters that are larger than 1/3 of an inch in diameter, even if they have not burst.</p></li>
                        </ul>
                        </p>
                        <h3>Step 5: Combat pain with over-the-counter medications.</h3>
                        <p>
                            Burns can be extremely painful. You may need painkillers to help you get through the day or fall asleep at night. Over-the-counter medications like non-steroidal anti-inflammatory drugs (NSAIDs) can be effective. However, they may interfere with other medications so discuss them with your doctor before taking them. Medications with aspirin should never be given to children.</p>
                        </p>
                        <h3>Step 6: Check to see if your tetanus shot is up to date.</h3>
                        <p>
                            Tetanus is a disease that occurs when the tetanus bacteria infect an open wound.Your doctor will likely suggest that you get a tetanus shot if:</p>
                        <ul>
                            <li><p>The burn caused a deep wound or it is dirty.</p></li>
                            <li><p>You haven’t had a tetanus shot in longer than five years.</p></li>
                            <li><p>You don’t know when your last tetanus shot was.</p></li>
                        </ul>
                        </p>
                        <h3>Step 7: Monitor the burn for signs of infections.</p></h3>
                        <p>
                            Your skin provides you with a barrier against pathogens in the environment. A burn makes you vulnerable to infection. If your burn develops any of the following symptoms, go to the emergency room right away to have it checked by a doctor.</p>
                        <ul>
                            <li><p>Pus or fluid seeping from the wound.</p></li>
                            <li><p>Swelling, redness, or pain that increases over time.</p></li>
                        </ul>
                        </p>
